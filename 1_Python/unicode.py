# Find Antonyms words

import nltk
nltk.download('omw-1.4')
from nltk.corpus import wordnet

nltk.download('wordnet')

antonyms=[]
for syn in wordnet.synsets("ahead"):
    for lm in syn.lemmas():
        if lm.antonyms():
            antonyms.append(lm.antonyms()[0].name())
            
print(set(antonyms))

































